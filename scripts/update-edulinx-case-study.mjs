;(async () => {
  console.log('1) Archiving existing children blocks...')
  let removed = 0
  let cursor
  do {
    const list = await notion.blocks.children.list({
      block_id: PAGE_ID,
      start_cursor: cursor,
      page_size: 100,
    })
    for (const b of list.results) {
      await notion.blocks.delete({ block_id: b.id })
      removed++
    }
    cursor = list.has_more ? list.next_cursor : undefined
  } while (cursor)
  console.log('   removed ' + removed + ' blocks')

  console.log('2) Updating properties...')
  await notion.pages.update({ page_id: PAGE_ID, properties: propertyUpdates })

  console.log('3) Appending new children (' + children.length + ' blocks)...')
  // Notion limits append to 100 blocks per call; we have < 100 so one call
  await notion.blocks.children.append({ block_id: PAGE_ID, children })

  console.log('DONE')
})().catch((e) => {
  console.error('ERR:', e.message)
  process.exit(1)
})
